# while loop
number = 20
while number <= 25:
    print(number)
    number += 1

# Decrementing Values
a = 10
while a >= 1:
    print(a)
    a -= 1

# for loop - it prints in a given range of value( series)
languages = ["python", "PHP", "Kotlin", "C++"]
for lang in languages: # Printing the languages on its own #lang can be substituted by any letter
    print(lang)

# Range
for x in range(10):
    print(x)

for b in range(15, 20):
    print(b)

for c in range(4, 30, 2):
    print(c)

