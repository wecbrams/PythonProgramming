# Arithmetic Operators
x = 67
y = 3
print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y)  # Modulus

# Comparison Operators
num1 = 78
num2 = 45
print(num1 < num2)
print(num1 > num2)
print(num1 <= num2)
print(num1 >= num2)
print(num1 == num2)
print(num1 != num2)

c = 14
c -= 2
print(c)

# Logical Operators
number1 = 8
number2 = 20
print(number1 > number2 and number2 > number1)
print(number1 > number2 or number2 > number1)
print(not (number1 > number2 or number2 > number1))
